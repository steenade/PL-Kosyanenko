def gcd(a, b):
    """Функция для нахождения НОД (наибольший общий делитель) двух чисел с помощью алгоритма Евклида."""
    while b:
        a, b = b, a % b
    return a

def lcm(a, b):
    """Функция для нахождения НОК (наименьшее общее кратное) двух чисел."""
    return (a * b) // gcd(a, b)

# Ввод двух натуральных чисел от пользователя
num1 = int(input("Введите первое натуральное число: "))
num2 = int(input("Введите второе натуральное число: "))

# Проверка, что числа натуральные
if num1 <= 0 or num2 <= 0:
    print("Пожалуйста, введите натуральные числа больше нуля.")
else:
    # Вычисление НОД и НОК
    gcd_value = gcd(num1, num2)
    lcm_value = lcm(num1, num2)

    # Вывод результатов
    print("НОД(", num1, ", ", num2, ") =", gcd_value)
    print("НОК(", num1, ", ", num2, ") =", lcm_value)
